    (
      :LOJA_ID,
      :RETAGUARDA_TERMINAL_ID,
      :LOG_ID_RET,
      0,

      0, --LOJA_ID_ENVOLVIDO SMALLINT,
      0, --TERMINAL_ID_ENVOLVIDO SMALLINT,
