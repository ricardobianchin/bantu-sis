  )
  AS
    -- FUNCIONARIO GAR LOGVAR LOGVARS VARS LOG VAR LOG VARLOG VARSLOG
    DECLARE FEATURE_ID ID_SHORT_DOM = 3;--FUNCIONARIO
    DECLARE RETAGUARDA_TERMINAL_ID ID_SHORT_DOM = 0;
